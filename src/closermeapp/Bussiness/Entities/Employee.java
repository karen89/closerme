package closermeapp.Bussiness.Entities;


public class Employee extends Visitor {
    private String role;

    public Employee(String name, String phone, String role) {
        super(name, phone);
        this.role = role;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
